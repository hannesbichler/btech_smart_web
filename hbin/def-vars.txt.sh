####################################################################################################
# PATTERNS for REGULAR EXPRESSIONs
####################################################################################################
# \. punkt
# . genau ein beliebiges zeichen
# .? .+ .* .{n} .{n,m} .{n,} .{,m}
# [X]? 0 oder 1 mal [X]{0,1}
# [X]+ 1 oder mehrere male [X]{1,}
# [X]* 0 oder mehrere male [X]{0,}
# [X]{n} oder [X]{n,m} oder [X]{n,} oder [X]{,m}
####################################################################################################
n_0_255='([0-9]|[1-9][0-9]|[1][0-9][0-9]|[2][0-4][0-9]|[2][5][0-5])'
n_1_254='([1-9]|[1-9][0-9]|[1][0-9][0-9]|[2][0-4][0-9]|[2][5][0-4])'
####################################################################################################
### 0 ###
# 0.0.0.0 bis 255.255.255.255 (all network addresses)
patna="^(${n_0_255}\.${n_0_255}\.${n_0_255}\.${n_0_255})$"
####################################################################################################
ip010="(10\.${n_0_255}\.${n_0_255}\.${n_1_254})"
ip172="(172\.1[6789]\.${n_0_255}\.${n_1_254}|172\.2[0-9]\.${n_0_255}\.${n_1_254}|172\.3[01]\.${n_0_255}\.${n_1_254})"
ip192="(192\.168\.${n_0_255}\.${n_1_254})"
####################################################################################################
### 1 + 2 ###
